public class Switch {
    public static void main(String[] args) {
        for (int i = 0; i < 15; i++) {
            switch (i) {
                case 0:
                    System.out.println("chinonso");
                    break;
                case 1:
                    System.out.println("chinyeaka");
                    break;
                case 2:
                    System.out.println("victor");
                    break;
                case 3:
                    System.out.println("devops");
                    break;
                case 4:
                    System.out.println("java");
                    break;

                default:
                    System.out.println("default");
            }
        }
    }
}
